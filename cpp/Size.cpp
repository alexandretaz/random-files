#include <iostream>

using namespace std;

int main(){
    cout << "int " << sizeof(int) << '\n';
    cout << "short " << sizeof(short) << '\n';
    cout << "long " << sizeof(long) << '\n';
    cout << "char " << sizeof(char) << '\n';
    cout << "bool " << sizeof(bool) << '\n';
    cout << "float " << sizeof(float) << '\n';
    cout << "double " << sizeof(double) << '\n';

    return 0;

}
